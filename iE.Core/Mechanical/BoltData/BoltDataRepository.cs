        /// <summary>
        /// Direct lookup – throws exception if size or rating not found.
        /// </summary>
        public static BoltInfo GetBoltInfo(string size, string rating)
        {
            if (!BoltData.TryGetValue(size, out var ratingData))
                throw new Exception($"Flange size '{size}' not found.");

            if (!ratingData.TryGetValue(rating, out var boltInfo))
                throw new Exception($"Flange rating '{rating}' not found for size {size}.");

            return boltInfo;
        }

        /// <summary>
        /// Lookup with fallback: if rating "400" not found, try "600".
        /// If rating "900" not found, try "1500".
        /// Direct match is always preferred first.
        /// </summary>
        public static BoltInfo GetBoltInfoWithFallback(string size, string rating)
        {
            if (!BoltData.TryGetValue(size, out var ratingData))
                throw new Exception($"Flange size '{size}' not found.");

            if (ratingData.TryGetValue(rating, out var boltInfo))
                return boltInfo;

            // Fallback logic
            string fallbackRating = rating switch
            {
                "400" => "600",
                "900" => "1500",
                _ => null
            };

            if (fallbackRating != null && ratingData.TryGetValue(fallbackRating, out var fallbackInfo))
                return fallbackInfo;

            throw new Exception($"Flange rating '{rating}' (and fallback '{fallbackRating}') not found for size {size}.");
        }
